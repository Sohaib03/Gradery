                VALUES (R.ASSIGNMENT_ID, :NEW.USER_ID, 0, null, null);
            END LOOP;
    END ;
/

create or replace trigger DELETE_ASSIGNED_ASSIGNMENTS_OF_LEFT_USER
	after delete
	on PARTICIPANT
	for each row when (OLD.ROLE = 'student')
