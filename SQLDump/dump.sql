create or replace trigger ASSIGN_PENDING_ASSIGNMENTS_TO_NEW_USER
	after insert
	on PARTICIPANT
	for each row when (NEW.ROLE = 'student')
DECLARE
    BEGIN
        FOR R IN (SELECT * FROM ASSIGNMENTS WHERE TEAM_ID = :NEW.TEAM_ID AND DEADLINE > SYSDATE)
