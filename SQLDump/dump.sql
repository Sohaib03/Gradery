create or replace trigger ADD_PARTICIPANT_DELETE_INVITATION
	after insert
	on PARTICIPANT
	for each row
BEGIN
        DELETE FROM INVITATION I WHERE I.USER_ID = :NEW.USER_ID AND I.TEAM_ID = :NEW.TEAM_ID ;
    END;
